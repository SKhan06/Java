package in.gettersetter;

public class Car {
    private String name;
    private String color;
    private int model;
    private int fuel;
    private int carOfPurchase;

    public Car(String name, String color, int model, int fuel, int carOfPurchase) {
        this.name = name;
        this.color = color;
        this.model = model;
        this.fuel = fuel;
        this.carOfPurchase = carOfPurchase;
    }

    public String getName(){
        return name;
    }
    public String getColor(){
        return color;
    }





}
