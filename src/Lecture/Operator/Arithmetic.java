package Lecture.Operator;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 9;
        int b = 7;
        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a/b);
        System.out.println(a%b);

        double x = 5;
        double y = 8;
        System.out.println(x-y);
        System.out.println(x+y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
